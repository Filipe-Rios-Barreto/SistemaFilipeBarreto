    private void btnConsultarActionPerformed(java.awt.event.ActionEvent evt) {

        FrbVendedorDAO dao = new FrbVendedorDAO();

        if (!txtNome.getText().equals("") && !txtComissao.getText().equals("")) {

            controllerVendedor.setList(
                (List) dao.listNomeComissao(
                    txtNome.getText(),
                    Double.parseDouble(txtComissao.getText())
                )
            );

        } else if (!txtNome.getText().equals("")) {

            controllerVendedor.setList(
                (List) dao.listNome(txtNome.getText())
            );

        } else if (!txtComissao.getText().equals("")) {

            controllerVendedor.setList(
                (List) dao.listComissao(
                    Double.parseDouble(txtComissao.getText())
                )
            );

        } else {
            controllerVendedor.setList(
                (List) dao.listAll()
            );
        }
    }

